package manzano;

public class ExercicioB {

		public static void main(String[] args) {
		try {
			System.out.println("Soma dos 100 primeiros números inteiros:");
			int cont=0;
			
			for (int i = cont; i <= 100; i++) {
	            cont=cont+ i;
	            System.out.println(cont);
	        }
			
		} catch (Exception e) {
			System.out.println("Opção inválida!");
			
		}}

	}
